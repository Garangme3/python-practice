#Task 2: Check if a number is even or odd
#My thought process:
# - I will check if a number is even or odd by using the modulus % operator.
# - If remainder is 0 when divided by 2, then it's even, else it's odd.

value = 10

if value % 2 == 0:
    print(f"{value} is an Even Number")
else:
    print(f"{value} is an Odd Number")
